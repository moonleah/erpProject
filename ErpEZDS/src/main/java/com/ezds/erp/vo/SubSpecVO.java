package com.ezds.erp.vo;



public class SubSpecVO {
	
private Integer subSpecId; 
private Integer subSpecPrdNo; 
private String  subSpecMaker;
private String  subSpecName;
private String  updateDate;

public Integer getSubSpecId() {
	return subSpecId;
}
public void setSubSpecId(Integer subSpecId) {
	this.subSpecId = subSpecId;
}
public Integer getSubSpecPrdNo() {
	return subSpecPrdNo;
}
public void setSubSpecPrdNo(Integer subSpecPrdNo) {
	this.subSpecPrdNo = subSpecPrdNo;
}
public String getSubSpecMaker() {
	return subSpecMaker;
}
public void setSubSpecMaker(String subSpecMaker) {
	this.subSpecMaker = subSpecMaker;
}
public String getSubSpecName() {
	return subSpecName;
}
public void setSubSpecName(String subSpecName) {
	this.subSpecName = subSpecName;
}
public String getUpdateDate() {
	return updateDate;
}
public void setUpdateDate(String updateDate) {
	this.updateDate = updateDate;
}


	

}
